    triggers: Sequence[str]
    cross_sells: Sequence[tuple[str, str]]
    secondary_offers: Sequence[tuple[str, str, str, str]]
    disclosures: Sequence[str]
    campaign_window: str = DEFAULT_WINDOW
    fulfillment: str = DEFAULT_FULFILLMENT


