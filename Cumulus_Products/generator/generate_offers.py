def offer_code(product_code: str) -> str:
    return product_code.replace("2026.04", "OFF-2026.05")


def category_label(spec: OfferSpec) -> str:
    return f"CAMPAIGN OFFER  -  {spec.category.upper()}"


def segment_campaign_positioning(spec: OfferSpec) -> str:
    if spec.segment == "retail":
        return (
            "This retail campaign is written for household acquisition, product "
            "activation, and relationship deepening. The offer should be used "
            "by bankers as a concrete reason to contact clients with a timely "
            "rate, fee, or incentive message tied to the actual product terms."
        )
    if spec.segment == "wealth":
        return (
            "This wealth campaign is written for advisory conversations where "
            "fit, suitability, disclosure, and tax considerations matter as much "
            "as the incentive. The offer should support a measured proposal, not "
            "replace the advisor's fiduciary or best-interest analysis."
        )
    return (
        "This commercial campaign is written for relationship managers and "
        "specialists who need a practical offer, an underwriting path, and a "
        "clear implementation story for treasury, lending, deposits, merchant, "
        "or card conversations."
    )


def qualification_rows(spec: OfferSpec) -> list[tuple[str, str, str]]:
    return [
        ("Campaign window", spec.campaign_window,
         "Offer must be accepted, opened, funded, booked, or implemented during the campaign window unless the final approval memo extends the period."),
        ("Qualifying action", spec.qualifying_action,
         "Banker records the action in FSC and attaches application, funding, or service-enrollment evidence."),
        ("Minimum requirement", spec.minimum_requirement,
         "System validation, account analysis, loan booking, treasury implementation, or advisor review confirms eligibility."),
        ("Verification", spec.verification,
         "Operations confirms criteria before incentive credit, rate adjustment, fee waiver, or pricing concession is posted."),
        ("Fulfillment", spec.fulfillment,
         "Campaign member status changes to Fulfilled after incentive posting and required disclosures are archived."),
    ]


def controls_rows(spec: OfferSpec) -> list[tuple[str, str, str]]:
    return [
        ("Rate and price basis", spec.base_terms,
         "Source values align to docs/PRODUCT_SPECS.md or the product brochure terms effective April 25, 2026."),
        ("Offer authority", spec.campaign_terms,
         "Campaign pricing is temporary and must be linked to the offer code before account opening, booking, or implementation."),
        ("FSC evidence", "Campaign member, product interest, opportunity, application, and fulfillment task",
         "Keep the offer code, acceptance date, channel, banker, and any exception approval on the client timeline."),
        ("Adverse-change control", "Do not imply a permanent rate, fee waiver, or approval outcome",
         "All client-facing copy must preserve variable-rate, underwriting, eligibility, and expiration language."),
        ("Demo-use control", "Cumulus Bank is fictitious and the offer is illustrative only",
         "Never use this document for real consumer marketing, regulatory filing, or actual financial-decision support."),
    ]


def offer_rows(spec: OfferSpec) -> list[tuple[str, str, str, str]]:
    rows = [
        ("Primary incentive", spec.base_terms, spec.campaign_terms, spec.client_value),
    ]
    rows.extend(spec.secondary_offers)
    rows.append((
        "Expiration and reprice",
        "Standard product terms apply after the promotional period.",
        "Offer code expires at 11:59 p.m. ET on the final campaign date.",
        "Prevents stale pricing from carrying into future applications or renewals.",
    ))
    return rows


def build_offer(spec: OfferSpec) -> Path:
    B.set_theme(spec.segment)
    out_dir = OUT_ROOT / spec.folder
    out_dir.mkdir(parents=True, exist_ok=True)
    out_path = out_dir / f"{spec.file_stem}_Offer.pdf"

    doc = B.BrochureDoc(
        str(out_path),
        product_name=f"{spec.product_name} Offer",
        product_code=offer_code(spec.product_code),
        category=f"Product Offer - {spec.category}",
        segment=spec.segment,
    )

    story = []
    story += B.hero_block(
        product_name=f"{spec.product_name} Offer",
        lede=f"{spec.campaign_name}: {spec.headline}",
        summary_rows=[
            ("Product", spec.product_name),
            ("Campaign", spec.campaign_name),
            ("Campaign window", spec.campaign_window),
            ("Primary incentive", spec.campaign_terms),
            ("Base product terms", spec.base_terms),
            ("Offer code", offer_code(spec.product_code)),
            ("FSC campaign", DEFAULT_CAMPAIGN),
            ("Fulfillment", spec.fulfillment),
        ],
        category_label=category_label(spec),
    )
    story += B.switch_to_body()

    story.append(B.section_header("Campaign overview", kicker="Current offer"))
    story.append(B.lead_para(
        f"{spec.campaign_name} positions {spec.product_name} with a current, "
        f"product-specific marketing offer: {spec.headline} "
        f"{segment_campaign_positioning(spec)}"
    ))
    story.append(B.callout_box(
        "Primary client promise",
        f"{spec.campaign_terms} The value proposition is {spec.client_value}",
    ))
    story.append(Spacer(1, 0.08 * inch))
    story.append(B.feature_grid([
        ("Offer hook", spec.headline),
        ("Product basis", spec.base_terms),
        ("Target client", " ".join(spec.audience[:2])),
        ("Conversion path", spec.qualifying_action),
    ], cols=2))

    story.append(PageBreak())
    story.append(B.section_header("Current offer economics",
                                  kicker="Pricing, rates, incentives"))
    story.append(B.body_para(
        "Use this table as the banker-facing offer card. It distinguishes the "
        "standard product economics from the campaign concession so pricing, "
        "rate, and incentive language stays clear in FSC tasks, email follow-up, "
        "and branch or specialist conversations."
    ))
    story.append(B.data_table(
        header=["Offer component", "Standard product terms",
                "Campaign offer", "Client value and conditions"],
        rows=offer_rows(spec),
        col_widths=[1.35 * inch, 1.95 * inch, 1.95 * inch, 2.05 * inch],
    ))

    story.append(Spacer(1, 0.10 * inch))
    story.append(B.section_header("Qualification and fulfillment",
                                  kicker="Eligibility rules"))
    story.append(B.data_table(
        header=["Requirement", "Campaign rule", "Evidence and timing"],
        rows=qualification_rows(spec),
        col_widths=[1.45 * inch, 2.55 * inch, 3.30 * inch],
    ))

    story.append(PageBreak())
    story.append(B.section_header("FSC campaign playbook",
                                  kicker="Targeting and triggers"))
    story.append(B.two_col(
        left_flowables=[
            B.sub_header("Best-fit audiences"),
            *B.bullet_list(spec.audience),
        ],
        right_flowables=[
            B.sub_header("Trigger events"),
            *B.bullet_list(spec.triggers),
        ],
    ))

    story.append(Spacer(1, 0.10 * inch))
    story.append(B.section_header("Recommended next best actions",
                                  kicker="Relationship expansion"))
    story.append(B.data_table(
        header=["Next best action", "Why it fits this offer"],
        rows=spec.cross_sells,
        col_widths=[2.2 * inch, 5.1 * inch],
    ))

    story.append(Spacer(1, 0.10 * inch))
    story.append(B.section_header("Client journey",
                                  kicker="Fulfillment timeline"))
    story.append(B.data_table(
        header=["Step", "Banker or system action", "Target timing"],
        rows=[
            ("1 - Identify",
             "Add the household, client, prospect, or business to the FSC campaign based on the audience and trigger rules.",
             "Day 0"),
            ("2 - Present",
             "Send the offer summary, confirm fit, and record consent to proceed with application, consultation, or implementation.",
             "Days 0-3"),
            ("3 - Validate",
             "Collect required documents, run product eligibility checks, and confirm that the offer code is attached before booking.",
             "Days 1-10"),
            ("4 - Open or book",
             "Open the account, book the loan, enroll the treasury service, activate the card program, or schedule the advisory engagement.",
             "Days 1-30"),
            ("5 - Fulfill",
             spec.fulfillment,
             "Up to 45 days after qualification"),
            ("6 - Deepen",
             "Create follow-up tasks for the cross-sell actions and update campaign attribution on the client timeline.",
             "Next review cycle"),
        ],
        col_widths=[1.15 * inch, 4.85 * inch, 1.30 * inch],
    ))

    story.append(PageBreak())
    story.append(B.section_header("Controls and disclosure guardrails",
                                  kicker="Compliance notes"))
    story.append(B.data_table(
        header=["Control area", "Required handling", "Why it matters"],
        rows=controls_rows(spec),
        col_widths=[1.65 * inch, 2.85 * inch, 2.80 * inch],
    ))

    story += B.disclosure_block(
        "Offer disclosures",
        FAMILY_DISCLOSURES[spec.family] + list(spec.disclosures) + [
            "This offer document is a fictitious Cumulus Bank demo asset. All rates, fees, incentives, dates, approvals, and product terms are illustrative only and do not represent an actual financial product, account agreement, or offer of credit.",
        ],
    )
    story += B.back_cover_block()

    doc.build(story)
    return out_path


